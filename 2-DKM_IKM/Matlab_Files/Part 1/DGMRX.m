function T = DGMRX(q)
D3 = 0.45;
RL4 = 0.45;
T4611 = cos(q(5)) *cos(q(6));  
T4612 = -cos(q(5))* sin(q(6));
T4631 = sin(q(5)) *cos(q(6));
T4632 = -sin(q(5))* sin(q(6));
T3611 = cos(q(4)) *T4611 - sin(q(4)) *sin(q(6));
T3612 = cos(q(4))* T4612 - sin(q(4))* cos(q(6));
T3613 = -cos(q(4))* sin(q(5));
T3631 = -sin(q(4)) *T4611 - cos(q(4)) *sin(q(6));
T3632 = -sin(q(4))*T4612 - cos(q(4))* cos(q(6));
T3633 = sin(q(4)) *sin(q(5));
T1314 = D3 *cos(q(2));
T1334 = D3* sin(q(2));
T1611 = cos(q(2) + q(3)) * T3611 - sin(q(2) + q(3)) *T4631;
T1612 = cos(q(2) + q(3)) * T3612 - sin(q(2) + q(3)) *T4632;
T1613 = cos(q(2) + q(3)) * T3613 - sin(q(2) + q(3))* cos(q(5));
T1614 = -sin(q(2) + q(3)) *RL4 + T1314;
T1631 = sin(q(2) + q(3)) * T3611 + cos(q(2) + q(3))* T4631;
T1632 = sin(q(2) + q(3)) * T3612 + cos(q(2) + q(3)) *T4632;
T1633 = sin(q(2) + q(3)) * T3613 + cos(q(2) + q(3))* cos(q(5));
T1634 = cos(q(2) + q(3)) * RL4 + T1334;
T = eye(4);
T(1,1) = cos(q(1))* T1611 + sin(q(1))* T3631;
T(1,2) = cos(q(1))* T1612 + sin(q(1)) *T3632;
T(1,3) = cos(q(1))* T1613 + sin(q(1))* T3633;
T(1,4) = cos(q(1))* T1614;
T(2,1) = sin(q(1))* T1611 - cos(q(1)) *T3631;
T(2,2) = sin(q(1))* T1612 - cos(q(1)) *T3632;
T(2,3) = sin(q(1))* T1613 - cos(q(1))* T3633;
T(2,4) = sin(q(1))* T1614;
T(3,1) = T1631;
T(3,2) = T1632;
T(3,3) = T1633;
T(3,4) = T1634;

end